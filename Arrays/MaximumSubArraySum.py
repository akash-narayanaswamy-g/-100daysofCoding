#53. Maximum Subarray
#https://leetcode.com/problems/maximum-subarray/description/

class Solution(object):
    def maxSubArray(self, A):
        if not A:
            return 0

        curSum = maxSum = A[0]
        for num in A[1:]:
            curSum = max(num, curSum + num)
            maxSum = max(maxSum, curSum)

        return maxSum